struct activity {
    ll  problem, time;
};

bool activity_compare(activity s1, activity s2) {
    if (s1.problem == s2.problem)
        return s1.time < s2.time;
    return s1.problem > s2.problem;
}

ll max(ll a, ll b)
{
    return (a > b) ? a : b;
}
ll min(ll a, ll b)
{
    return (a < b) ? a : b;
}

